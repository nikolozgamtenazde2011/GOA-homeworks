#print() - გამოიყენება ტექსტის ან რიცხვის ეკრანზე გამოსატანად
#input() - გამოიყენება მომხმარებლისგან მონაცემების მისაღებად
#len() - გამოიყენება სტრიქონის ან სიის სიგრძის გასაგებად
#type() - გამოიყენება მონაცემის ტიპის გასაგებად
