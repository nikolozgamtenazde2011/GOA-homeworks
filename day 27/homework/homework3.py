#int() - გამოიყენება მონაცემის მთელი რიცხვის ტიპად გადაქცევისთვის
#float() - გამოიყენება მონაცემის ათწილადი რიცხვის ტიპად გადაქცევისთვის
#bool() - გამოიყენება მონაცემის ლოგიკური ტიპად გადაქცევისთვის (True ან False)

